    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        Thread t = new Thread(new Runnable() {

            @Override
            public void run() {
                progressSpin.setValue(0);
                progressSpin.setIndeterminate(true);
                if (!txtSource.getText().isEmpty()) {
                    UserAgent userAgent = new UserAgent();
                    try {
                        StringBuilder sb = new StringBuilder();
                        sb.append("act=spintax&")
                                .append("text=").append(txtSource.getText()).append("&")
                                .append("maxsyns=").append(spinScore.getValue()).append("&")
                                .append("score=").append(cmbQuality.getSelectedIndex() + 1);
                        if (!txtExclude.getText().isEmpty()) {
                            sb.append("&protectedterms=").append(txtExclude.getText());
                        }
                        userAgent.sendPOST(txtApiCall.getText(), sb.toString());
                        txtSpintax.setText(userAgent.doc.innerHTML());

                    } catch (ResponseException ex) {
                        txtSpintax.setText(ex.getMessage());
                    }

                }
                
                progressSpin.setValue(100);
                progressSpin.setIndeterminate(false);
            }
        });
        t.start();
    }//GEN-LAST:event_jButton2ActionPerformed

