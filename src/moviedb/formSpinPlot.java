    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        Thread t = new Thread(new Runnable() {

            @Override
            public void run() {
                progressSpin.setValue(0);
                progressSpin.setIndeterminate(true);
                if (!txtSpintax.getText().isEmpty()) {
                    UserAgent userAgent = new UserAgent();
                    try {
                        StringBuilder sb = new StringBuilder();
                        sb.append("act=randspin&")
                                .append("text=").append(txtSpintax.getText());

                        userAgent.sendPOST(txtApiCall.getText(), sb.toString());
                        txtResult.setText(userAgent.doc.innerHTML().replace("\\", ""));

                    } catch (ResponseException ex) {
                        txtResult.setText(ex.getMessage());
                    }

                }
                progressSpin.setValue(100);
                progressSpin.setIndeterminate(false);
            }
        });

        t.start();

    }//GEN-LAST:event_jButton1ActionPerformed

